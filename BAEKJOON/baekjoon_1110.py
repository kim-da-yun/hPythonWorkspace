num = int(input()) 
n = num
cnt = 0 # 사이클 수

while True:
    a = n // 10 # 입력 값을 10으로 나눈 정수 부분의 몫 
    b = n % 10 # 연산 후 나머지 값 
    c = (a + b) % 10
    n = (b * 10) + c
    
    cnt += 1
    
    if(n == num):
        break

print(cnt)